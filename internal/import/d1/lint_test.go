func TestParseDump(t *testing.T) {
	tables, err := ParseDump(testFixture(t))
	if err != nil {
		t.Fatalf("ParseDump: %v", err)
	}
	if len(tables) != 6 {
		t.Fatalf("expected 6 tables, got %d", len(tables))
	}
	if tables[0].Name != "users" {
		t.Fatalf("expected users table first, got %s", tables[0].Name)
	}

	var teamMembers *TableSchema
	for i := range tables {
		if tables[i].Name == "entity_links" {
			teamMembers = &tables[i]
			break
		}
	}
	if teamMembers == nil {
		t.Fatal("expected entity_links table")
	}
	if len(teamMembers.Constraints) < 2 {
		t.Fatalf("expected composite PK and FK constraints, got %v", teamMembers.Constraints)
	}
}

