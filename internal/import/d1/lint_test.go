func TestStateStore(t *testing.T) {
	t.Setenv("PSCALE_TEST_MODE", "1")

	store, err := NewStateStore()
	if err != nil {
		t.Fatal(err)
	}

	state := &MigrationState{
		MigrationID: "test123",
		Org:         "acme",
		Database:    "mydb",
		Branch:      "main",
		InputPath:   testFixture(t),
		Phase:       PhasePlanned,
	}
	if err := store.Save(state); err != nil {
		t.Fatal(err)
	}

	loaded, err := store.Load("acme", "mydb", "main", "test123")
	if err != nil {
		t.Fatal(err)
	}
	if loaded.MigrationID != "test123" {
		t.Fatalf("expected test123, got %s", loaded.MigrationID)
	}

	if err := store.Delete("acme", "mydb", "main", "test123"); err != nil {
		t.Fatal(err)
	}
}

