func TestConvertSchema(t *testing.T) {
	out := t.TempDir() + "/schema.sql"
	count, err := ConvertSchema(testFixture(t), out)
	if err != nil {
		t.Fatalf("ConvertSchema: %v", err)
	}
	if count != 4 {
		t.Fatalf("expected 4 tables converted, got %d", count)
	}
	data, err := os.ReadFile(out)
	if err != nil {
		t.Fatal(err)
	}
	content := string(data)
	checks := []string{
		"GENERATED BY DEFAULT AS IDENTITY",
		"BOOLEAN",
		"TIMESTAMPTZ",
		`FOREIGN KEY ("user_id") REFERENCES "users" ("id")`,
		`PRIMARY KEY ("entity_id", "post_id")`,
		`"id" UUID PRIMARY KEY`,
		`"entity_id" UUID NOT NULL`,
		`CREATE INDEX IF NOT EXISTS "idx_users_email"`,
		`CREATE UNIQUE INDEX IF NOT EXISTS "idx_entity_links_post"`,
		`UNIQUE`,
		`"id" INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY`,
	}
	for _, check := range checks {
		if !strings.Contains(content, check) {
			t.Fatalf("expected schema to contain %q\n%s", check, content)
		}
	}
	if strings.Contains(content, "__drizzle_migrations") || strings.Contains(content, "_prisma_migrations") {
		t.Fatal("ORM metadata tables should be skipped in schema output")
	}
}

