func TestLint(t *testing.T) {
	result, err := Lint(testFixture(t))
	if err != nil {
		t.Fatalf("Lint: %v", err)
	}
	if result.TableCount != 6 {
		t.Fatalf("expected 6 tables, got %d", result.TableCount)
	}
	if result.ErrorCount != 0 {
		t.Fatalf("expected no errors, got %d", result.ErrorCount)
	}
	if result.WarningCount == 0 {
		t.Fatal("expected warnings for autoincrement/boolean columns")
	}

	foundAutoincrement := false
	foundDrizzle := false
	for _, issue := range result.Issues {
		if issue.Code == "AUTOINCREMENT" {
			foundAutoincrement = true
		}
		if issue.Code == "DRIZZLE_MIGRATIONS" {
			foundDrizzle = true
		}
	}
	if !foundAutoincrement {
		t.Fatal("expected AUTOINCREMENT issue")
	}
	if !foundDrizzle {
		t.Fatal("expected DRIZZLE_MIGRATIONS issue")
	}
}

